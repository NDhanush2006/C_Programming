/*DoWhile :- it is similar like while loop
'in While' it checks the condition first&then executes the code
'do-while' executes the code atleast once & then it checks the condition
*/
#include<stdio.h>

int main(){

    int i =0;
    do
    {
        printf("The value of i is: %d\n",i);
        i++;
    } while (i<4);
    

    return 0;
}
