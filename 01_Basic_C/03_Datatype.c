/* Constants :- An enity whose value Doesn;t change is called Constant
   Variable :- A variable is an enity whose value can be changed

   Integr constant - 1,5, 7
   Character constant - a, A, @, &
*/


#include<stdio.h>

int main(){

    int a = 11;

    float b = 2.8;

    char c = 'A'; 
    printf("The integer is : %d\n", a);

    printf("The floating number is : %f\n", b);

    printf("The single character is : %c\n", c);
 
    return 0;
}